package me.imflowow.tritium.client.cape;

public class ReplacementString {
	private String name, replace;

	public ReplacementString(String name, String replace) {
		this.name = name;
		this.replace = replace;
	}

	public String getName() {
		return name;
	}

	public String getReplace() {
		return replace;
	}

}
